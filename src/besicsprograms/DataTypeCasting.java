package besicsprograms;

public class DataTypeCasting {

	public static void main(String[] args) {
		
		byte b = 10;
		int i = b;
		
		System.out.println(i);
		
		int i1 = 10;
		byte b1 = (byte)i1;
		System.out.println(b1);
		
		 
		int c=100;
		long n = c;
		System.out.println(n);
		
		 long l2 = 1000;
		 float f1 = l2;
		 System.out.println(f1);
		 
		 float f3 = 12.33f;
		 long l4 = (long)f3;
		 System.out.println(l4);
		 

	}

}
